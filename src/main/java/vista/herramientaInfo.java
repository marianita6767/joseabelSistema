    public void mostrarHerramienta(HerramientaDatos herramienta) {
        txtCodigo.setText(herramienta.getCodigo());
        txtNombre.setText(herramienta.getNombre());
        txtTipo.setText(herramienta.getTipo());
        txtMarca.setText(herramienta.getMarca());
        txtEstado.setText(herramienta.getEstado());
        txtDetalles.setText(herramienta.getDetalles());

        // Cargar imagen si existe
        if (herramienta.getImagen() != null) {
            lblImagen.setIcon(herramienta.getImagen()); 
        } else {
            lblImagen.setText("No hay imagen");
            lblImagen.setIcon(null);
        }
    }

